# 매달 투자할 금액과 투자 기간을 설정합니다.
매달투자금액 = 30  # 매달 투자할 금액
매달_이율 = 0.01  # 매달 1%의 수익률
투자기간 = 336 # 투자할 기간 (개월)

# 초기 투자 및 총 투자 변수 초기화
입금금액 = 0
최종금액 = 0

# 매달 투자 결과 계산
for month in range(1, 투자기간 + 1):
    # 매달 새로운 투자를 추가
    입금금액 += 매달투자금액
    최종금액 += 매달투자금액
    
    # 기존 투자에 이자를 적용
    최종금액 *= (1 + 매달_이율)
    
    # 해당 월의 결과 출력
    print(f"{month}달 동안 {입금금액}만원 으로 {최종금액:.1f}만원을 만들었습니다!")